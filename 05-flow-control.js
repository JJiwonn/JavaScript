// While문 버전
console.log("For문 구구단")
for(let dan = 2; dan <= 9; dna++) {
    for(let i = 1; i < 10; i++) {
        console.log("dan * " + i + " = " + dan*i);
    }
}


console.log("While문 구구단")
let i = 1;
while (i <= 9) {
    console.log("2 * " + i + " = " + 2*i);
    i++;
}

